package codegen

import (
	"bytes"
	"fmt"
	"strconv"
	"strings"
	"unicode"

	shunter "github.com/ponchione/shunter"
	"github.com/ponchione/shunter/protocol"
	"github.com/ponchione/shunter/schema"
)

type namedTypeScriptIdentifier struct {
	name          string
	identifier    string
	sql           string
	contractIndex int
}

type typeScriptGenerationOptions struct {
	runtimeImport string
	profile       string
}

type typeScriptRuntimeValueImports struct {
	callReducerWithEncodedArgs bool
	callReducerWithResult      bool
	decodeBsatnProduct         bool
	decodeDeclaredQueryResult  bool
	encodeBsatnProduct         bool
}

type typeScriptTableFacade struct {
	identifier        string
	name              string
	rowType           string
	subscribeFunction string
	eventFunction     string
	isEvent           bool
}

type typeScriptReducerFacade struct {
	identifier        string
	name              string
	argsType          string
	rawFunction       string
	typedFunction     string
	resultFunction    string
	hasTypedArguments bool
}

type typeScriptProcedureFacade struct {
	identifier        string
	name              string
	argsType          string
	rawFunction       string
	typedFunction     string
	hasTypedArguments bool
}

type typeScriptQueryFacade struct {
	identifier      string
	name            string
	paramsType      string
	rowsType        string
	runFunction     string
	decodedFunction string
	hasRows         bool
}

type typeScriptViewFacade struct {
	identifier        string
	name              string
	paramsType        string
	subscribeFunction string
	handleFunction    string
	rowType           string
}

type typeScriptColumns interface {
	Len() int
	At(i int) (name, kind string, nullable bool)
}

type typeScriptTableColumns []schema.ColumnExport
type typeScriptProductColumns []schema.ProductColumnExport

func (columns typeScriptTableColumns) Len() int { return len(columns) }

func (columns typeScriptTableColumns) At(i int) (string, string, bool) {
	column := columns[i]
	return column.Name, column.Type, column.Nullable
}

func (columns typeScriptProductColumns) Len() int { return len(columns) }

func (columns typeScriptProductColumns) At(i int) (string, string, bool) {
	column := columns[i]
	return column.Name, column.Type, column.Nullable
}

func generateTypeScript(contract shunter.ModuleContract, opts typeScriptGenerationOptions) ([]byte, error) {
	profile, err := normalizedProfile(opts.profile)
	if err != nil {
		return nil, err
	}
	profileTables := typeScriptProfileTables(contract.Schema.Tables, profile)
	var b bytes.Buffer

	b.WriteString("// Code generated by github.com/ponchione/shunter/codegen. DO NOT EDIT.\n")
	fmt.Fprintf(&b, "// Source contract: %s v%d\n", contract.Codegen.ContractFormat, contract.Codegen.ContractVersion)
	if contract.Module.Name != "" || contract.Module.Version != "" {
		fmt.Fprintf(&b, "// Module: %s %s\n", typeScriptLineCommentText(contract.Module.Name), typeScriptLineCommentText(contract.Module.Version))
	}
	b.WriteString("\n")
	writeTypeScriptRuntimeImports(&b, opts.runtimeImport)
	writeTypeScriptRuntimeValueImports(&b, opts.runtimeImport, typeScriptRuntimeValueImportUsage(contract, profileTables))
	if err := writeTypeScriptProtocolMetadata(&b); err != nil {
		return nil, err
	}
	writeTypeScriptContractMetadata(&b, contract, profile, opts.runtimeImport)
	b.WriteString("export type ReducerCaller = ShunterReducerCaller<ReducerName, Uint8Array, Uint8Array>;\n")
	b.WriteString("export type ProcedureCaller = ShunterProcedureCaller<ProcedureName, Uint8Array, Uint8Array>;\n")
	b.WriteString("export type EncodedReducerCallOptions<Args = unknown> = ShunterEncodedReducerCallOptions<Args>;\n")
	b.WriteString("export type EncodedReducerCallResultOptions<Args = unknown, Result = Uint8Array> = ShunterEncodedReducerCallResultOptions<Args, Result>;\n")
	b.WriteString("export type ReducerCallResult<Name extends ReducerName = ReducerName, Result = Uint8Array> = ShunterReducerCallResult<Name, Result>;\n")
	b.WriteString("export type ReducerCallResultOptions = ShunterReducerCallResultRequestOptions<Uint8Array>;\n")
	b.WriteString("export type QueryRunner = ShunterQueryRunner<Uint8Array>;\n")
	b.WriteString("export type ViewSubscriber = ShunterViewSubscriber;\n")
	b.WriteString("export type DeclaredQueryRunner = ShunterDeclaredQueryRunner<ExecutableQueryName, Uint8Array>;\n")
	b.WriteString("export type DeclaredQueryOptions = ShunterDeclaredQueryOptions;\n")
	b.WriteString("export type DeclaredQueryRunOptions = Omit<DeclaredQueryOptions, \"params\">;\n")
	b.WriteString("export type RawDeclaredQueryResult<Name extends ExecutableQueryName = ExecutableQueryName> = ShunterRawDeclaredQueryResult<Name>;\n")
	b.WriteString("export type DeclaredQueryDecodeOptions<RowsByName extends object = TableRows> = ShunterDeclaredQueryDecodeOptions<RowsByName>;\n")
	b.WriteString("export type DeclaredQueryDecodedRunOptions<RowsByName extends object = TableRows> = DeclaredQueryRunOptions & DeclaredQueryDecodeOptions<RowsByName>;\n")
	b.WriteString("export type DecodedDeclaredQueryResult<Name extends ExecutableQueryName = ExecutableQueryName, RowsByName extends object = TableRows> = ShunterDecodedDeclaredQueryResult<Name, RowsByName>;\n")
	b.WriteString("export type DeclaredViewSubscriber = ShunterDeclaredViewSubscriber<ExecutableViewName>;\n")
	b.WriteString("export type DeclaredViewHandleSubscriber = ShunterDeclaredViewHandleSubscriber<ExecutableViewName>;\n")
	b.WriteString("export type DeclaredViewSubscriptionOptions<Row = unknown> = Omit<ShunterDeclaredViewSubscriptionOptions<Row>, \"params\">;\n")
	b.WriteString("export type DeclaredViewSubscribeOptions<Row = unknown> = Omit<DeclaredViewSubscriptionOptions<Row>, \"returnHandle\"> & { readonly returnHandle?: false | undefined };\n")
	b.WriteString("export type SubscriptionUnsubscribe = ShunterSubscriptionUnsubscribe;\n")
	b.WriteString("export type SubscriptionHandle<Row = unknown> = ShunterSubscriptionHandle<Row>;\n")
	b.WriteString("export type SubscriptionHandleReturnOptions = ShunterSubscriptionHandleReturnOptions;\n")
	b.WriteString("export type SubscriptionRowEvent<Row = unknown> = ShunterSubscriptionRowEvent<Row>;\n")
	b.WriteString("export type TableRow<Name extends TableName> = TableRows[Name];\n")
	b.WriteString("export type TableSubscriber<Row = never> = ShunterTableSubscriber<TableName, TableRows, Row>;\n")
	b.WriteString("export type TableSubscriptionOptions<Row = unknown> = ShunterTableSubscriptionOptions<Row>;\n")
	b.WriteString("export type TableSubscribeOptions<Row = unknown> = Omit<TableSubscriptionOptions<Row>, \"returnHandle\"> & { readonly returnHandle?: false | undefined };\n")
	b.WriteString("export type TableSubscribeCaller<Row = never> = <Table extends TableName>(table: Table, onRows?: (rows: ([Row] extends [never] ? TableRows[Table] : Row)[]) => void, options?: TableSubscribeOptions<[Row] extends [never] ? TableRows[Table] : Row>) => Promise<SubscriptionUnsubscribe>;\n")
	b.WriteString("export type TableRowDecoder<Name extends TableName> = ShunterTableRowDecoder<TableRows[Name]>;\n")
	b.WriteString("export type TableRowDecoders<RowsByName extends object = TableRows> = ShunterTableRowDecoders<RowsByName>;\n")
	b.WriteString("export type UUID = string;\n")
	b.WriteString("\n")

	tableNames := make([]string, len(profileTables))
	for i, table := range profileTables {
		tableNames[i] = table.Name
	}
	tableTypes := uniqueTypeScriptIdentifiers(tableNames, typeScriptPascalIdentifier)
	tableConstants := uniqueTypeScriptIdentifiers(tableNames, typeScriptCamelIdentifier)
	topLevelValueNames := typeScriptTopLevelValueNames()
	tableRowTypes := typeScriptTableRowTypes(tableTypes, topLevelValueNames)
	for i, table := range profileTables {
		rowType := tableRowTypes[i]
		if _, err := writeTypeScriptInterface(&b, rowType, typeScriptTableColumns(table.Columns)); err != nil {
			return nil, err
		}
	}

	writeTypeScriptConstMap(&b, "tables", tableConstants)
	b.WriteString("export type TableName = (typeof tables)[keyof typeof tables];\n\n")
	writeTypeScriptTableRows(&b, profileTables, tableRowTypes)
	if err := writeTypeScriptTableRowDecoders(&b, profileTables, tableRowTypes, topLevelValueNames); err != nil {
		return nil, err
	}
	writeTypeScriptTableReadPolicies(&b, profileTables, tableConstants)
	writeTypeScriptVisibilityFilters(&b, contract.VisibilityFilters)
	tableFacades := make([]typeScriptTableFacade, 0, len(profileTables))
	for i, table := range profileTables {
		rowType := tableRowTypes[i]
		functionName := uniqueTypeScriptIdentifier("subscribe"+tableTypes[i].identifier, topLevelValueNames)
		tableFacade := typeScriptTableFacade{
			identifier:        tableConstants[i].identifier,
			name:              table.Name,
			rowType:           rowType,
			subscribeFunction: functionName,
			isEvent:           table.IsEvent,
		}
		fmt.Fprintf(&b, "export function %s(subscribeTable: TableSubscribeCaller<%s>, onRows?: (rows: %s[]) => void, options: TableSubscribeOptions<%s> = {}): Promise<SubscriptionUnsubscribe> {\n", functionName, rowType, rowType, rowType)
		fmt.Fprintf(&b, "  const subscribeOptions: TableSubscribeOptions<%s> = options.decodeRow === undefined ? { ...options, decodeRow: tableRowDecoders[%s]%s } : { ...options%s };\n", rowType, strconv.Quote(table.Name), typeScriptEventTableOptionSuffix(table.IsEvent), typeScriptEventTableOptionSuffix(table.IsEvent))
		fmt.Fprintf(&b, "  return subscribeTable(%s, onRows, subscribeOptions);\n", strconv.Quote(table.Name))
		b.WriteString("}\n\n")
		if table.IsEvent {
			eventFunctionName := uniqueTypeScriptIdentifier(functionName+"Inserts", topLevelValueNames)
			tableFacade.eventFunction = eventFunctionName
			fmt.Fprintf(&b, "export function %s(subscribeTable: TableSubscribeCaller<%s>, onInsert: (event: SubscriptionRowEvent<%s>) => void, options: TableSubscribeOptions<%s> = {}): Promise<SubscriptionUnsubscribe> {\n", eventFunctionName, rowType, rowType, rowType)
			fmt.Fprintf(&b, "  const subscribeOptions: TableSubscribeOptions<%s> = options.decodeRow === undefined ? { ...options, decodeRow: tableRowDecoders[%s], eventTable: true, onInsert } : { ...options, eventTable: true, onInsert };\n", rowType, strconv.Quote(table.Name))
			fmt.Fprintf(&b, "  return subscribeTable(%s, undefined, subscribeOptions);\n", strconv.Quote(table.Name))
			b.WriteString("}\n\n")
		}
		tableFacades = append(tableFacades, tableFacade)
	}

	reducerNames := make([]string, 0, len(contract.Schema.Reducers))
	lifecycleReducerNames := make([]string, 0, len(contract.Schema.Reducers))
	for _, reducer := range contract.Schema.Reducers {
		if reducer.Lifecycle {
			lifecycleReducerNames = append(lifecycleReducerNames, reducer.Name)
			continue
		}
		reducerNames = append(reducerNames, reducer.Name)
	}
	reducerIdentifiers := uniqueTypeScriptIdentifiers(reducerNames, typeScriptCamelIdentifier)
	writeTypeScriptConstMap(&b, "reducers", reducerIdentifiers)
	b.WriteString("export type ReducerName = (typeof reducers)[keyof typeof reducers];\n\n")
	reducersByName := make(map[string]schema.ReducerExport, len(contract.Schema.Reducers))
	for _, reducer := range contract.Schema.Reducers {
		reducersByName[reducer.Name] = reducer
	}
	reducerFacades := make([]typeScriptReducerFacade, 0, len(reducerIdentifiers))
	for _, reducer := range reducerIdentifiers {
		reducerExport := reducersByName[reducer.name]
		var argsTypeName, argsEncoderName string
		argsTypeName, argsEncoderName, _, err := writeTypeScriptProductCodecVariant(&b, topLevelValueNames, reducer.identifier, "Args", "ArgsColumns", "Args", "", reducerExport.Args, true, false)
		if err != nil {
			return nil, err
		}
		if _, _, _, err := writeTypeScriptProductCodecVariant(&b, topLevelValueNames, reducer.identifier, "Result", "ResultColumns", "", "Result", reducerExport.Result, false, true); err != nil {
			return nil, err
		}
		functionName := uniqueTypeScriptIdentifier("call"+upperFirst(reducer.identifier), topLevelValueNames)
		reducerFacade := typeScriptReducerFacade{
			identifier:     reducer.identifier,
			name:           reducer.name,
			argsType:       argsTypeName,
			rawFunction:    functionName,
			resultFunction: uniqueTypeScriptIdentifier(functionName+"Result", topLevelValueNames),
		}
		fmt.Fprintf(&b, "export function %s(callReducer: ReducerCaller, args: Uint8Array): Promise<Uint8Array> {\n", functionName)
		fmt.Fprintf(&b, "  return callReducer(%s, args);\n", strconv.Quote(reducer.name))
		b.WriteString("}\n\n")
		if reducerExport.Args != nil {
			typedFunctionName := uniqueTypeScriptIdentifier(functionName+"Typed", topLevelValueNames)
			reducerFacade.typedFunction = typedFunctionName
			reducerFacade.hasTypedArguments = true
			fmt.Fprintf(&b, "export function %s(callReducer: ReducerCaller, args: %s, options: EncodedReducerCallOptions<%s> = {}): Promise<Uint8Array> {\n", typedFunctionName, argsTypeName, argsTypeName)
			fmt.Fprintf(&b, "  const callOptions: EncodedReducerCallOptions<%s> = options.encodeArgs === undefined ? { ...options, encodeArgs: %s } : options;\n", argsTypeName, argsEncoderName)
			fmt.Fprintf(&b, "  return shunterCallReducerWithEncodedArgs(callReducer, %s, args, callOptions);\n", strconv.Quote(reducer.name))
			b.WriteString("}\n\n")
		}
		resultFunctionName := reducerFacade.resultFunction
		fmt.Fprintf(&b, "export function %s(callReducer: ReducerCaller, args: Uint8Array, options: ReducerCallResultOptions = {}): Promise<ReducerCallResult<typeof reducers.%s>> {\n", resultFunctionName, reducer.identifier)
		fmt.Fprintf(&b, "  return shunterCallReducerWithResult(callReducer, %s, args, options);\n", strconv.Quote(reducer.name))
		b.WriteString("}\n\n")
		reducerFacades = append(reducerFacades, reducerFacade)
	}
	lifecycleReducerIdentifiers := uniqueTypeScriptIdentifiers(lifecycleReducerNames, typeScriptLifecycleIdentifier)
	writeTypeScriptConstMap(&b, "lifecycleReducers", lifecycleReducerIdentifiers)
	b.WriteString("export type LifecycleReducerName = (typeof lifecycleReducers)[keyof typeof lifecycleReducers];\n\n")

	procedureNames := make([]string, len(contract.Procedures))
	for i, procedure := range contract.Procedures {
		procedureNames[i] = procedure.Name
	}
	procedureIdentifiers := uniqueTypeScriptIdentifiers(procedureNames, typeScriptCamelIdentifier)
	writeTypeScriptConstMap(&b, "procedures", procedureIdentifiers)
	b.WriteString("export type ProcedureName = (typeof procedures)[keyof typeof procedures];\n\n")
	proceduresByName := make(map[string]shunter.ProcedureDescription, len(contract.Procedures))
	for _, procedure := range contract.Procedures {
		proceduresByName[procedure.Name] = procedure
	}
	procedureFacades := make([]typeScriptProcedureFacade, 0, len(procedureIdentifiers))
	for _, procedure := range procedureIdentifiers {
		procedureExport := proceduresByName[procedure.name]
		var argsTypeName, argsEncoderName string
		argsTypeName, argsEncoderName, _, err := writeTypeScriptProductCodecVariant(&b, topLevelValueNames, procedure.identifier, "Args", "ArgsColumns", "Args", "", procedureExport.Args, true, false)
		if err != nil {
			return nil, err
		}
		if _, _, _, err := writeTypeScriptProductCodecVariant(&b, topLevelValueNames, procedure.identifier, "Result", "ResultColumns", "", "Result", procedureExport.Result, false, true); err != nil {
			return nil, err
		}
		functionName := uniqueTypeScriptIdentifier("call"+upperFirst(procedure.identifier)+"Procedure", topLevelValueNames)
		procedureFacade := typeScriptProcedureFacade{
			identifier:  procedure.identifier,
			name:        procedure.name,
			argsType:    argsTypeName,
			rawFunction: functionName,
		}
		fmt.Fprintf(&b, "export function %s(callProcedure: ProcedureCaller, args: Uint8Array): Promise<Uint8Array> {\n", functionName)
		fmt.Fprintf(&b, "  return callProcedure(%s, args);\n", strconv.Quote(procedure.name))
		b.WriteString("}\n\n")
		if procedureExport.Args != nil {
			typedFunctionName := uniqueTypeScriptIdentifier(functionName+"Typed", topLevelValueNames)
			procedureFacade.typedFunction = typedFunctionName
			procedureFacade.hasTypedArguments = true
			fmt.Fprintf(&b, "export function %s(callProcedure: ProcedureCaller, args: %s): Promise<Uint8Array> {\n", typedFunctionName, argsTypeName)
			fmt.Fprintf(&b, "  return callProcedure(%s, %s(args));\n", strconv.Quote(procedure.name), argsEncoderName)
			b.WriteString("}\n\n")
		}
		procedureFacades = append(procedureFacades, procedureFacade)
	}

	queryNames := make([]string, len(contract.Queries))
	for i, query := range contract.Queries {
		queryNames[i] = query.Name
	}
	queryIdentifiers := uniqueTypeScriptIdentifiers(queryNames, typeScriptCamelIdentifier)
	writeTypeScriptConstMap(&b, "queries", queryIdentifiers)
	b.WriteString("export type QueryName = (typeof queries)[keyof typeof queries];\n\n")
	executableQueries := executableQueryIdentifiers(contract.Queries, queryIdentifiers)
	writeTypeScriptSQLConstMap(&b, "querySQL", executableQueries)
	b.WriteString("export type ExecutableQueryName = (typeof queries)[keyof typeof querySQL];\n\n")
	queryFacades := make([]typeScriptQueryFacade, 0, len(executableQueries))
	for _, query := range executableQueries {
		queryDescription := contract.Queries[query.contractIndex]
		var queryParamsTypeName, queryParamsEncoderName string
		if typeScriptDeclaredReadHasParameters(queryDescription.Parameters) {
			var err error
			queryParamsTypeName, queryParamsEncoderName, _, err = writeTypeScriptProductCodecVariant(&b, topLevelValueNames, query.identifier, "Params", "ParamColumns", "Params", "", queryDescription.Parameters, true, false)
			if err != nil {
				return nil, err
			}
		}
		var queryRowsTypeName, queryRowDecodersName string
		if queryDescription.RowSchema != nil && queryDescription.ResultShape != nil {
			rowTypeName, _, decoderName, err := writeTypeScriptProductCodecVariant(&b, topLevelValueNames, query.identifier, "QueryRow", "QueryColumns", "", "QueryRow", queryDescription.RowSchema, false, true)
			if err != nil {
				return nil, err
			}
			queryRowsTypeName = uniqueTypeScriptIdentifier(upperFirst(query.identifier)+"QueryRows", topLevelValueNames)
			queryRowDecodersName = uniqueTypeScriptIdentifier(query.identifier+"QueryRowDecoders", topLevelValueNames)
			fmt.Fprintf(&b, "export type %s = {\n", queryRowsTypeName)
			fmt.Fprintf(&b, "  %s: %s;\n", strconv.Quote(queryDescription.ResultShape.Table), rowTypeName)
			b.WriteString("};\n\n")
			fmt.Fprintf(&b, "export const %s = {\n", queryRowDecodersName)
			fmt.Fprintf(&b, "  %s: %s,\n", strconv.Quote(queryDescription.ResultShape.Table), decoderName)
			fmt.Fprintf(&b, "} as const satisfies TableRowDecoders<%s>;\n\n", queryRowsTypeName)
		}
		functionName := uniqueTypeScriptIdentifier("query"+upperFirst(query.identifier), topLevelValueNames)
		queryFacade := typeScriptQueryFacade{
			identifier:  query.identifier,
			name:        query.name,
			paramsType:  queryParamsTypeName,
			runFunction: functionName,
		}
		if queryParamsTypeName != "" {
			fmt.Fprintf(&b, "export function %s(runDeclaredQuery: DeclaredQueryRunner, params: %s, options: DeclaredQueryRunOptions = {}): Promise<Uint8Array> {\n", functionName, queryParamsTypeName)
			fmt.Fprintf(&b, "  return runDeclaredQuery(%s, { ...options, params: %s(params) });\n", strconv.Quote(query.name), queryParamsEncoderName)
		} else {
			fmt.Fprintf(&b, "export function %s(runDeclaredQuery: DeclaredQueryRunner): Promise<Uint8Array> {\n", functionName)
			fmt.Fprintf(&b, "  return runDeclaredQuery(%s);\n", strconv.Quote(query.name))
		}
		b.WriteString("}\n\n")
		resultFunctionName := uniqueTypeScriptIdentifier(functionName+"Result", topLevelValueNames)
		if queryRowsTypeName != "" {
			fmt.Fprintf(&b, "export function %s(data: unknown, options: DeclaredQueryDecodeOptions<%s> = {}): DecodedDeclaredQueryResult<typeof queries.%s, %s> {\n", resultFunctionName, queryRowsTypeName, query.identifier, queryRowsTypeName)
			fmt.Fprintf(&b, "  const decodeOptions: DeclaredQueryDecodeOptions<%s> = options.tableDecoders === undefined && options.decodeRow === undefined ? { ...options, tableDecoders: %s } : options;\n", queryRowsTypeName, queryRowDecodersName)
			fmt.Fprintf(&b, "  return shunterDecodeDeclaredQueryResult(%s, data, decodeOptions);\n", strconv.Quote(query.name))
			b.WriteString("}\n\n")
			decodedFunctionName := uniqueTypeScriptIdentifier(functionName+"Decoded", topLevelValueNames)
			queryFacade.decodedFunction = decodedFunctionName
			queryFacade.rowsType = queryRowsTypeName
			queryFacade.hasRows = true
			if queryParamsTypeName != "" {
				fmt.Fprintf(&b, "export async function %s(runDeclaredQuery: DeclaredQueryRunner, params: %s, options: DeclaredQueryDecodedRunOptions<%s> = {}): Promise<DecodedDeclaredQueryResult<typeof queries.%s, %s>> {\n", decodedFunctionName, queryParamsTypeName, queryRowsTypeName, query.identifier, queryRowsTypeName)
				b.WriteString("  const queryOptions: DeclaredQueryOptions = {\n")
				b.WriteString("    requestId: options.requestId,\n")
				b.WriteString("    messageId: options.messageId,\n")
				b.WriteString("    signal: options.signal,\n")
				fmt.Fprintf(&b, "    params: %s(params),\n", queryParamsEncoderName)
				b.WriteString("  };\n")
				fmt.Fprintf(&b, "  const decodeOptions: DeclaredQueryDecodeOptions<%s> = {\n", queryRowsTypeName)
				b.WriteString("    tableDecoders: options.tableDecoders,\n")
				b.WriteString("    decodeRow: options.decodeRow,\n")
				b.WriteString("  };\n")
				fmt.Fprintf(&b, "  return %s(await runDeclaredQuery(%s, queryOptions), decodeOptions);\n", resultFunctionName, strconv.Quote(query.name))
			} else {
				fmt.Fprintf(&b, "export async function %s(runDeclaredQuery: DeclaredQueryRunner, options: DeclaredQueryDecodeOptions<%s> = {}): Promise<DecodedDeclaredQueryResult<typeof queries.%s, %s>> {\n", decodedFunctionName, queryRowsTypeName, query.identifier, queryRowsTypeName)
				fmt.Fprintf(&b, "  return %s(await runDeclaredQuery(%s), options);\n", resultFunctionName, strconv.Quote(query.name))
			}
			b.WriteString("}\n\n")
		} else {
			fmt.Fprintf(&b, "export function %s<RowsByName extends object = TableRows>(data: unknown, options: DeclaredQueryDecodeOptions<RowsByName>): DecodedDeclaredQueryResult<typeof queries.%s, RowsByName> {\n", resultFunctionName, query.identifier)
			fmt.Fprintf(&b, "  return shunterDecodeDeclaredQueryResult(%s, data, options);\n", strconv.Quote(query.name))
			b.WriteString("}\n\n")
		}
		queryFacades = append(queryFacades, queryFacade)
	}

	viewNames := make([]string, len(contract.Views))
	for i, view := range contract.Views {
		viewNames[i] = view.Name
	}
	viewIdentifiers := uniqueTypeScriptIdentifiers(viewNames, typeScriptCamelIdentifier)
	writeTypeScriptConstMap(&b, "views", viewIdentifiers)
	b.WriteString("export type ViewName = (typeof views)[keyof typeof views];\n\n")
	executableViews := executableViewIdentifiers(contract.Views, viewIdentifiers)
	writeTypeScriptSQLConstMap(&b, "viewSQL", executableViews)
	b.WriteString("export type ExecutableViewName = (typeof views)[keyof typeof viewSQL];\n\n")
	viewFacades := make([]typeScriptViewFacade, 0, len(executableViews))
	for _, view := range executableViews {
		viewDescription := contract.Views[view.contractIndex]
		var viewParamsTypeName, viewParamsEncoderName string
		if typeScriptDeclaredReadHasParameters(viewDescription.Parameters) {
			var err error
			viewParamsTypeName, viewParamsEncoderName, _, err = writeTypeScriptProductCodecVariant(&b, topLevelValueNames, view.identifier, "Params", "ParamColumns", "Params", "", viewDescription.Parameters, true, false)
			if err != nil {
				return nil, err
			}
		}
		var rowTypeName, decoderName string
		if viewDescription.RowSchema != nil {
			var err error
			rowTypeName, _, decoderName, err = writeTypeScriptProductCodecVariant(&b, topLevelValueNames, view.identifier, "ViewRow", "ViewColumns", "", "ViewRow", viewDescription.RowSchema, false, true)
			if err != nil {
				return nil, err
			}
		}
		functionName := uniqueTypeScriptIdentifier("subscribe"+upperFirst(view.identifier), topLevelValueNames)
		viewFacade := typeScriptViewFacade{
			identifier:        view.identifier,
			name:              view.name,
			paramsType:        viewParamsTypeName,
			subscribeFunction: functionName,
			rowType:           rowTypeName,
		}
		if rowTypeName != "" {
			if viewParamsTypeName != "" {
				fmt.Fprintf(&b, "export function %s(subscribeDeclaredView: DeclaredViewSubscriber, params: %s, options: DeclaredViewSubscribeOptions<%s> = {}): Promise<SubscriptionUnsubscribe> {\n", functionName, viewParamsTypeName, rowTypeName)
			} else {
				fmt.Fprintf(&b, "export function %s(subscribeDeclaredView: DeclaredViewSubscriber, options: DeclaredViewSubscribeOptions<%s> = {}): Promise<SubscriptionUnsubscribe> {\n", functionName, rowTypeName)
			}
			fmt.Fprintf(&b, "  const subscribeOptions: DeclaredViewSubscribeOptions<%s> = options.decodeRow === undefined ? { ...options, decodeRow: %s } : options;\n", rowTypeName, decoderName)
			if viewParamsTypeName != "" {
				fmt.Fprintf(&b, "  return subscribeDeclaredView(%s, { ...subscribeOptions, params: %s(params) });\n", strconv.Quote(view.name), viewParamsEncoderName)
			} else {
				fmt.Fprintf(&b, "  return subscribeDeclaredView(%s, subscribeOptions);\n", strconv.Quote(view.name))
			}
			b.WriteString("}\n\n")
			handleFunctionName := uniqueTypeScriptIdentifier(functionName+"Handle", topLevelValueNames)
			viewFacade.handleFunction = handleFunctionName
			if viewParamsTypeName != "" {
				fmt.Fprintf(&b, "export function %s(subscribeDeclaredView: DeclaredViewHandleSubscriber, params: %s, options: DeclaredViewSubscriptionOptions<%s> & SubscriptionHandleReturnOptions): Promise<SubscriptionHandle<%s>> {\n", handleFunctionName, viewParamsTypeName, rowTypeName, rowTypeName)
			} else {
				fmt.Fprintf(&b, "export function %s(subscribeDeclaredView: DeclaredViewHandleSubscriber, options: DeclaredViewSubscriptionOptions<%s> & SubscriptionHandleReturnOptions): Promise<SubscriptionHandle<%s>> {\n", handleFunctionName, rowTypeName, rowTypeName)
			}
			fmt.Fprintf(&b, "  const subscribeOptions: DeclaredViewSubscriptionOptions<%s> & SubscriptionHandleReturnOptions = options.decodeRow === undefined ? { ...options, decodeRow: %s } : options;\n", rowTypeName, decoderName)
			if viewParamsTypeName != "" {
				fmt.Fprintf(&b, "  return subscribeDeclaredView(%s, { ...subscribeOptions, params: %s(params) });\n", strconv.Quote(view.name), viewParamsEncoderName)
			} else {
				fmt.Fprintf(&b, "  return subscribeDeclaredView(%s, subscribeOptions);\n", strconv.Quote(view.name))
			}
			b.WriteString("}\n\n")
		} else {
			if viewParamsTypeName != "" {
				fmt.Fprintf(&b, "export function %s(subscribeDeclaredView: DeclaredViewSubscriber, params: %s, options: DeclaredViewSubscribeOptions = {}): Promise<SubscriptionUnsubscribe> {\n", functionName, viewParamsTypeName)
				fmt.Fprintf(&b, "  return subscribeDeclaredView(%s, { ...options, params: %s(params) });\n", strconv.Quote(view.name), viewParamsEncoderName)
			} else {
				fmt.Fprintf(&b, "export function %s(subscribeDeclaredView: DeclaredViewSubscriber): Promise<SubscriptionUnsubscribe> {\n", functionName)
				fmt.Fprintf(&b, "  return subscribeDeclaredView(%s);\n", strconv.Quote(view.name))
			}
			b.WriteString("}\n\n")
		}
		viewFacades = append(viewFacades, viewFacade)
	}

	writeTypeScriptPermissions(&b, contract.Permissions)
	writeTypeScriptReadModels(&b, contract.ReadModel.Declarations)
	writeTypeScriptModuleClient(&b, tableFacades, reducerFacades, procedureFacades, queryFacades, viewFacades)

	return b.Bytes(), nil
}

