func writeTypeScriptModuleClient(
	b *bytes.Buffer,
	tables []typeScriptTableFacade,
	reducers []typeScriptReducerFacade,
	procedures []typeScriptProcedureFacade,
	queries []typeScriptQueryFacade,
	views []typeScriptViewFacade,
) {
	b.WriteString("export interface ModuleClientBindings {\n")
	b.WriteString("  readonly callReducer: ReducerCaller;\n")
	b.WriteString("  readonly callProcedure: ProcedureCaller;\n")
	b.WriteString("  readonly runDeclaredQuery: DeclaredQueryRunner;\n")
	b.WriteString("  readonly subscribeDeclaredView: DeclaredViewSubscriber & DeclaredViewHandleSubscriber;\n")
	b.WriteString("  readonly subscribeTable: TableSubscribeCaller & (<Table extends string, Row = Table extends TableName ? TableRows[Table] : Uint8Array>(table: Table, onRows?: (rows: Row[]) => void, options?: TableSubscriptionOptions<Row>) => Promise<SubscriptionUnsubscribe | SubscriptionHandle<Row>>);\n")
	b.WriteString("}\n\n")

	b.WriteString("export function createModuleClient(bindings: ModuleClientBindings) {\n")
	b.WriteString("  return {\n")
	b.WriteString("    reducers: {\n")
	for _, reducer := range reducers {
		fmt.Fprintf(b, "      %s: {\n", reducer.identifier)
		if reducer.hasTypedArguments {
			fmt.Fprintf(b, "        call: (args: %s, options: EncodedReducerCallOptions<%s> = {}) => %s(bindings.callReducer, args, options),\n", reducer.argsType, reducer.argsType, reducer.typedFunction)
			fmt.Fprintf(b, "        raw: (args: Uint8Array) => %s(bindings.callReducer, args),\n", reducer.rawFunction)
		} else {
			fmt.Fprintf(b, "        call: (args: Uint8Array) => %s(bindings.callReducer, args),\n", reducer.rawFunction)
		}
		fmt.Fprintf(b, "        result: (args: Uint8Array, options: ReducerCallResultOptions = {}) => %s(bindings.callReducer, args, options),\n", reducer.resultFunction)
		b.WriteString("      },\n")
	}
	b.WriteString("    },\n")

	b.WriteString("    procedures: {\n")
	for _, procedure := range procedures {
		fmt.Fprintf(b, "      %s: {\n", procedure.identifier)
		if procedure.hasTypedArguments {
			fmt.Fprintf(b, "        call: (args: %s) => %s(bindings.callProcedure, args),\n", procedure.argsType, procedure.typedFunction)
			fmt.Fprintf(b, "        raw: (args: Uint8Array) => %s(bindings.callProcedure, args),\n", procedure.rawFunction)
		} else {
			fmt.Fprintf(b, "        call: (args: Uint8Array) => %s(bindings.callProcedure, args),\n", procedure.rawFunction)
		}
		b.WriteString("      },\n")
	}
	b.WriteString("    },\n")

	b.WriteString("    queries: {\n")
	for _, query := range queries {
		fmt.Fprintf(b, "      %s: {\n", query.identifier)
		if query.paramsType != "" {
			fmt.Fprintf(b, "        run: (params: %s, options: DeclaredQueryRunOptions = {}) => %s(bindings.runDeclaredQuery, params, options),\n", query.paramsType, query.runFunction)
			if query.hasRows {
				fmt.Fprintf(b, "        decoded: (params: %s, options: DeclaredQueryDecodedRunOptions<%s> = {}) => %s(bindings.runDeclaredQuery, params, options),\n", query.paramsType, query.rowsType, query.decodedFunction)
			}
		} else {
			fmt.Fprintf(b, "        run: (options: DeclaredQueryRunOptions = {}) => bindings.runDeclaredQuery(%s, options),\n", strconv.Quote(query.name))
			if query.hasRows {
				fmt.Fprintf(b, "        decoded: (options: DeclaredQueryDecodeOptions<%s> = {}) => %s(bindings.runDeclaredQuery, options),\n", query.rowsType, query.decodedFunction)
			}
		}
		b.WriteString("      },\n")
	}
	b.WriteString("    },\n")

	b.WriteString("    views: {\n")
	for _, view := range views {
		fmt.Fprintf(b, "      %s: {\n", view.identifier)
		if view.paramsType != "" {
			fmt.Fprintf(b, "        subscribe: (params: %s, options: DeclaredViewSubscribeOptions<%s> = {}) => %s(bindings.subscribeDeclaredView, params, options),\n", view.paramsType, typeScriptViewFacadeRowType(view), view.subscribeFunction)
			if view.handleFunction != "" {
				fmt.Fprintf(b, "        handle: (params: %s, options: DeclaredViewSubscriptionOptions<%s> & SubscriptionHandleReturnOptions) => %s(bindings.subscribeDeclaredView, params, options),\n", view.paramsType, view.rowType, view.handleFunction)
			}
		} else {
			if view.rowType != "" {
				fmt.Fprintf(b, "        subscribe: (options: DeclaredViewSubscribeOptions<%s> = {}) => %s(bindings.subscribeDeclaredView, options),\n", view.rowType, view.subscribeFunction)
				if view.handleFunction != "" {
					fmt.Fprintf(b, "        handle: (options: DeclaredViewSubscriptionOptions<%s> & SubscriptionHandleReturnOptions) => %s(bindings.subscribeDeclaredView, options),\n", view.rowType, view.handleFunction)
				}
			} else {
				fmt.Fprintf(b, "        subscribe: () => %s(bindings.subscribeDeclaredView),\n", view.subscribeFunction)
			}
		}
		b.WriteString("      },\n")
	}
	b.WriteString("    },\n")

	b.WriteString("    tables: {\n")
	for _, table := range tables {
		fmt.Fprintf(b, "      %s: {\n", table.identifier)
		fmt.Fprintf(b, "        subscribe: (onRows?: (rows: %s[]) => void, options: TableSubscribeOptions<%s> = {}) => %s(bindings.subscribeTable, onRows, options),\n", table.rowType, table.rowType, table.subscribeFunction)
		b.WriteString("      },\n")
	}
	b.WriteString("    },\n")

	b.WriteString("    events: {\n")
	for _, table := range tables {
		if !table.isEvent {
			continue
		}
		fmt.Fprintf(b, "      %s: {\n", table.identifier)
		fmt.Fprintf(b, "        onInsert: (handler: (event: SubscriptionRowEvent<%s>) => void, options: TableSubscribeOptions<%s> = {}) => %s(bindings.subscribeTable, handler, options),\n", table.rowType, table.rowType, table.eventFunction)
		b.WriteString("      },\n")
	}
	b.WriteString("    },\n")
	b.WriteString("  } as const;\n")
	b.WriteString("}\n\n")
}

func typeScriptViewFacadeRowType(view typeScriptViewFacade) string {
	if view.rowType == "" {
		return "unknown"
	}
	return view.rowType
}

func writeTypeScriptPermissions(b *bytes.Buffer, permissions shunter.PermissionContract) {
	b.WriteString("export const permissions = {\n")
	writeTypeScriptPermissionCategory(b, "reducers", permissions.Reducers)
	writeTypeScriptPermissionCategory(b, "queries", permissions.Queries)
	writeTypeScriptPermissionCategory(b, "views", permissions.Views)
	b.WriteString("} as const;\n\n")
}

func writeTypeScriptPermissionCategory(b *bytes.Buffer, category string, declarations []shunter.PermissionContractDeclaration) {
	fmt.Fprintf(b, "  %s: {\n", category)
	identifiers := permissionTypeScriptIdentifiers(declarations)
	for i, identifier := range identifiers {
		fmt.Fprintf(b, "    %s: { required: %s },\n", identifier.identifier, typeScriptStringArray(declarations[i].Required))
	}
	b.WriteString("  },\n")
}

func permissionTypeScriptIdentifiers(declarations []shunter.PermissionContractDeclaration) []namedTypeScriptIdentifier {
	names := make([]string, len(declarations))
	for i, declaration := range declarations {
		names[i] = declaration.Name
	}
	return uniqueTypeScriptIdentifiers(names, typeScriptCamelIdentifier)
}

func writeTypeScriptReadModels(b *bytes.Buffer, declarations []shunter.ReadModelContractDeclaration) {
	b.WriteString("export const readModels = {\n")
	writeTypeScriptReadModelCategory(b, "queries", declarations, shunter.ReadModelSurfaceQuery)
	writeTypeScriptReadModelCategory(b, "views", declarations, shunter.ReadModelSurfaceView)
	b.WriteString("} as const;\n\n")
}

func writeTypeScriptReadModelCategory(b *bytes.Buffer, category string, declarations []shunter.ReadModelContractDeclaration, surface string) {
	filtered := make([]shunter.ReadModelContractDeclaration, 0, len(declarations))
	for _, declaration := range declarations {
		if declaration.Surface == surface {
			filtered = append(filtered, declaration)
		}
	}

	fmt.Fprintf(b, "  %s: {\n", category)
	identifiers := readModelTypeScriptIdentifiers(filtered)
	for i, identifier := range identifiers {
		declaration := filtered[i]
		fmt.Fprintf(
			b,
			"    %s: { tables: %s, tags: %s },\n",
			identifier.identifier,
			typeScriptStringArray(declaration.Tables),
			typeScriptStringArray(declaration.Tags),
		)
	}
	b.WriteString("  },\n")
}

func readModelTypeScriptIdentifiers(declarations []shunter.ReadModelContractDeclaration) []namedTypeScriptIdentifier {
	names := make([]string, len(declarations))
	for i, declaration := range declarations {
		names[i] = declaration.Name
	}
	return uniqueTypeScriptIdentifiers(names, typeScriptCamelIdentifier)
}

func visibilityFilterTypeScriptIdentifiers(filters []shunter.VisibilityFilterDescription) []namedTypeScriptIdentifier {
	names := make([]string, len(filters))
	for i, filter := range filters {
		names[i] = filter.Name
	}
	return uniqueTypeScriptIdentifiers(names, typeScriptCamelIdentifier)
}

func typeScriptStringArray(values []string) string {
	if len(values) == 0 {
		return "[]"
	}
	quoted := make([]string, len(values))
	for i, value := range values {
		quoted[i] = strconv.Quote(value)
	}
	return "[" + strings.Join(quoted, ", ") + "]"
}

var typeScriptLineCommentEscaper = strings.NewReplacer(
	"\r", `\r`,
	"\n", `\n`,
	"\u2028", `\u2028`,
	"\u2029", `\u2029`,
)

func typeScriptLineCommentText(value string) string {
	return typeScriptLineCommentEscaper.Replace(value)
}

func typeScriptColumnType(kind string) (string, error) {
	switch kind {
	case "bool":
		return "boolean", nil
	case "int8", "uint8", "int16", "uint16", "int32", "uint32", "float32", "float64":
		return "number", nil
	case "int64", "uint64", "int128", "uint128", "int256", "uint256", "timestamp", "duration":
		return "bigint", nil
	case "string":
		return "string", nil
	case "uuid":
		return "UUID", nil
	case "bytes":
		return "Uint8Array", nil
	case "arrayString":
		return "string[]", nil
	case "json":
		return "unknown", nil
	default:
		return "", fmt.Errorf("unsupported column type %q", kind)
	}
}

func uniqueTypeScriptIdentifiers(names []string, style func(string) string) []namedTypeScriptIdentifier {
	out := make([]namedTypeScriptIdentifier, len(names))
	seen := make(map[string]int, len(names))
	for i, name := range names {
		base := style(name)
		if base == "" {
			base = "_"
		}
		identifier := uniqueTypeScriptIdentifier(base, seen)
		out[i] = namedTypeScriptIdentifier{name: name, identifier: identifier}
	}
	return out
}

func uniqueTypeScriptIdentifier(base string, seen map[string]int) string {
	identifier := base
	if count := seen[identifier]; count > 0 {
		for suffix := count + 1; ; suffix++ {
			candidate := fmt.Sprintf("%s%d", base, suffix)
			if seen[candidate] == 0 {
				identifier = candidate
				break
			}
		}
	}
	seen[identifier]++
	return identifier
}

func typeScriptCamelIdentifier(name string) string {
	words, hadSeparator := identifierWords(name)
	if len(words) == 0 {
		return "_"
	}
	var b strings.Builder
	for i, word := range words {
		normalized := normalizeIdentifierWord(word)
		if i == 0 {
			if hadSeparator {
				normalized = lowerFirst(normalized)
			}
			b.WriteString(normalized)
			continue
		}
		b.WriteString(upperFirst(normalized))
	}
	return ensureTypeScriptIdentifier(b.String())
}

func typeScriptPascalIdentifier(name string) string {
	words, _ := identifierWords(name)
	if len(words) == 0 {
		return "_"
	}
	var b strings.Builder
	for _, word := range words {
		b.WriteString(upperFirst(normalizeIdentifierWord(word)))
	}
	return ensureTypeScriptIdentifier(b.String())
}

func typeScriptLifecycleIdentifier(name string) string {
	return ensureTypeScriptIdentifier(typeScriptPascalIdentifier(name))
}

func identifierWords(name string) ([]string, bool) {
	var words []string
	var b strings.Builder
	hadSeparator := false
	for _, r := range name {
		if unicode.IsLetter(r) || unicode.IsDigit(r) {
			b.WriteRune(r)
			continue
		}
		hadSeparator = true
		if b.Len() > 0 {
			words = append(words, b.String())
			b.Reset()
		}
	}
	if b.Len() > 0 {
		words = append(words, b.String())
	}
	return words, hadSeparator
}

func normalizeIdentifierWord(word string) string {
	if word == "" {
		return ""
	}
	if isAllUpperOrDigit(word) || isAllLowerOrDigit(word) {
		return strings.ToLower(word)
	}
	return word
}

func ensureTypeScriptIdentifier(identifier string) string {
	if identifier == "" {
		return "_"
	}
	var b strings.Builder
	for i, r := range identifier {
		if i == 0 && unicode.IsDigit(r) {
			b.WriteRune('_')
			b.WriteRune(r)
			continue
		}
		valid := r == '_' || r == '$' || unicode.IsLetter(r) || unicode.IsDigit(r)
		if !valid {
			b.WriteRune('_')
			continue
		}
		b.WriteRune(r)
	}
	out := b.String()
	if out == "" {
		return "_"
	}
	if typeScriptReservedWords[out] {
		return out + "_"
	}
	return out
}

func upperFirst(s string) string {
	if s == "" {
		return ""
	}
	runes := []rune(s)
	runes[0] = unicode.ToUpper(runes[0])
	return string(runes)
}

func lowerFirst(s string) string {
	if s == "" {
		return ""
	}
	runes := []rune(s)
	runes[0] = unicode.ToLower(runes[0])
	return string(runes)
}

func isAllUpperOrDigit(s string) bool {
	hasLetter := false
	for _, r := range s {
		if !unicode.IsLetter(r) {
			continue
		}
		hasLetter = true
		if unicode.IsLower(r) {
			return false
		}
	}
	return hasLetter
}

func isAllLowerOrDigit(s string) bool {
	hasLetter := false
	for _, r := range s {
		if !unicode.IsLetter(r) {
			continue
		}
		hasLetter = true
		if unicode.IsUpper(r) {
			return false
		}
	}
	return hasLetter
}

